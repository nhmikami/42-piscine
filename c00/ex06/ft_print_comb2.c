#include <unistd.h>

void	ft_print_comb2(void)
{
	char	comb[5];
	int		i;
	int		j;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			comb[0] = '0' + (i / 10);
			comb[1] = '0' + (i % 10);
			comb[2] = ' ';
			comb[3] = '0' + (j / 10);
			comb[4] = '0' + (j % 10);
			write(1, &comb, 5);
			if (!(i == 98 && j == 99))
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
	return ;
}
